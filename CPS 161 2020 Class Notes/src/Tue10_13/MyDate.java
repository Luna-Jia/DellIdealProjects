package Tue10_13;



public class MyDate {
    private int year;
    private String month;
    private int day;

    public MyDate() {
        year = 2020;
        month = "Oct";
        day = 13;
    }

    public MyDate(int year, String month, int day) {
        this.year = year;
        this.month = month;
        this.day = day;
    }

    public int getYear() {
        return year;
    }

    public int getYear() {
        return year;
    }

}
